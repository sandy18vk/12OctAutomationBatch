package core_Java_Testprg;

public class LoopsinJava {
	public static void main(String args[]) {
		
		
	for(int i=1; i<=5; i++) {
			
			System.out.println(i);
		}
		
	System.out.println("*************************");	
		int i=5;
		while (i<=10) {
			System.out.println(i);
			i++;
			
		}
		System.out.println("*************************");
		int j=1;
		do {
			System.out.println(j);
			j++;
					} while (j<=10);
		

		
	}
	                        
}
 